int logit(std::string message)
{
    auto timenow = chrono::system_clock::to_time_t(chrono::system_clock::now()); 
    cout << "LOG: " << ctime(&timenow) << " Thread: " << std::this_thread::get_id() << message << endl;

    return 0;
}